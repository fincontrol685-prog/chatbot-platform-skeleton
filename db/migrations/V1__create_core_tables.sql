CREATE TABLE ROLE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(50) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR2(4000),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE USER_ACCOUNT (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME VARCHAR2(100) NOT NULL UNIQUE,
    EMAIL VARCHAR2(255),
    PASSWORD_HASH VARCHAR2(4000) NOT NULL,
    ENABLED NUMBER(1) DEFAULT 1,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    LAST_LOGIN TIMESTAMP,
    VERSION NUMBER DEFAULT 0
);

CREATE TABLE USER_ROLE (
    USER_ID NUMBER NOT NULL,
    ROLE_ID NUMBER NOT NULL,
    CONSTRAINT PK_USER_ROLE PRIMARY KEY (USER_ID, ROLE_ID),
    CONSTRAINT FK_UR_USER FOREIGN KEY (USER_ID) REFERENCES USER_ACCOUNT(ID),
    CONSTRAINT FK_UR_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLE(ID)
);

CREATE TABLE BOT (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(255) NOT NULL,
    BOT_KEY VARCHAR2(255) NOT NULL UNIQUE,
    ENABLED NUMBER(1) DEFAULT 1,
    OWNER_ID NUMBER,
    CONFIG CLOB,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    VERSION NUMBER DEFAULT 0,
    CONSTRAINT FK_BOT_OWNER FOREIGN KEY (OWNER_ID) REFERENCES USER_ACCOUNT(ID)
);

CREATE TABLE FLOW (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BOT_ID NUMBER NOT NULL,
    NAME VARCHAR2(255) NOT NULL,
    TYPE VARCHAR2(50),
    DEFINITION CLOB,
    ENABLED NUMBER(1) DEFAULT 1,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    VERSION NUMBER DEFAULT 0,
    CONSTRAINT FK_FLOW_BOT FOREIGN KEY (BOT_ID) REFERENCES BOT(ID)
);

CREATE TABLE INTENT (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BOT_ID NUMBER NOT NULL,
    NAME VARCHAR2(255) NOT NULL,
    CONFIDENCE_THRESHOLD NUMBER(5,4) DEFAULT 0.50,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT FK_INTENT_BOT FOREIGN KEY (BOT_ID) REFERENCES BOT(ID)
);

CREATE TABLE UTTERANCE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    INTENT_ID NUMBER NOT NULL,
    TEXT VARCHAR2(4000) NOT NULL,
    CONSTRAINT FK_UTTER_INTENT FOREIGN KEY (INTENT_ID) REFERENCES INTENT(ID)
);

CREATE TABLE SESSION (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BOT_ID NUMBER NOT NULL,
    USER_ID NUMBER,
    STATE CLOB,
    STARTED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    ENDED_AT TIMESTAMP,
    CONSTRAINT FK_SESSION_BOT FOREIGN KEY (BOT_ID) REFERENCES BOT(ID),
    CONSTRAINT FK_SESSION_USER FOREIGN KEY (USER_ID) REFERENCES USER_ACCOUNT(ID)
);

CREATE TABLE MESSAGE_HISTORY (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SESSION_ID NUMBER NOT NULL,
    SENDER VARCHAR2(50),
    MESSAGE_TEXT CLOB,
    METADATA CLOB,
    TIMESTAMP TIMESTAMP DEFAULT SYSTIMESTAMP,
    CONSTRAINT FK_MSG_SESSION FOREIGN KEY (SESSION_ID) REFERENCES SESSION(ID)
);

CREATE TABLE METRIC (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BOT_ID NUMBER,
    METRIC_TYPE VARCHAR2(100),
    METRIC_VALUE NUMBER,
    CAPTURED_AT TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX IDX_USER_USERNAME ON USER_ACCOUNT(USERNAME);
